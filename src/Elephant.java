public class Elephant extends Herbivorous implements Animal {

    public void eat() {
        System.out.println("The elephant is eating leaves and branches.");
    }

    public void sleep() {
        System.out.println("The elephant is sleeping standing up.");
    }

    public void makeSound() {
        System.out.println("The elephant is trumpeting loudly.");
    }

    public void move() {
        System.out.println("The elephant is walking slowly.");
    }
    public void graze() {
        System.out.println("The Elephant is grazing on grass.");
    }

}